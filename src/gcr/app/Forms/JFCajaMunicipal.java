    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double cantidadPrestada = Double.parseDouble(txtCP.getText());
        double tasaInteresAnual = (double) spInteres.getValue();
        int numeroCuotas = (int) spCuotas.getValue();
        
        double tasaInteresMensual = tasaInteresAnual / 12 / 100;
        double interesMensual = cantidadPrestada * tasaInteresMensual;
        double interesTotal = interesMensual * numeroCuotas;
        double totalPagar = cantidadPrestada + interesTotal;
        txtTotal.setText(""+totalPagar);

        if (numeroCuotas < 6) {
            totalPagar = totalPagar - 30;
            txtDescuento.setText("S/.30");
        } else if (numeroCuotas <= 12) {
            totalPagar *= 0.97;
            txtDescuento.setText("3%");
        } else {
            totalPagar *= 0.95;
            txtDescuento.setText("5%");
        }
        
        double mensualidad = totalPagar / numeroCuotas;
        
        txtInteresEnS.setText(String.format("%.2f", interesTotal));
        txtTotalPagar.setText(String.format("%.2f", totalPagar));
        txtMensualidad.setText(String.format("%.2f", mensualidad));     
    }//GEN-LAST:event_btnCalcularActionPerformed

